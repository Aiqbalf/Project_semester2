    private void btn_selesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selesaiActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_po.getSelectedRow();
    System.out.println("Selected row: " + selectedRow);

    if (selectedRow != -1) {
        int idPreorder = Integer.parseInt(tbl_po.getValueAt(selectedRow, 0).toString());
        System.out.println("ID yang dipilih: " + idPreorder);

        try {
            java.sql.Connection conn = Config.configDB();
            String sql = "UPDATE preorder SET status = 'selesai' WHERE id = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, idPreorder);

            int rows = stmt.executeUpdate();
            System.out.println("Baris yang diperbarui: " + rows);

            if (rows > 0) {
                // Anda bisa update tampilan manual:
                tbl_po.setValueAt("selesai", selectedRow, 7);

                // atau lebih baik: refresh data
//                loadTable(); // Pastikan Anda punya fungsi ini

                JOptionPane.showMessageDialog(null, "Status berhasil diubah ke selesai.");
            } else {
                JOptionPane.showMessageDialog(null, "Tidak ada data yang diperbarui. Periksa ID.");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal memperbarui status.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Pilih salah satu baris terlebih dahulu.");
    }
    }//GEN-LAST:event_btn_selesaiActionPerformed

