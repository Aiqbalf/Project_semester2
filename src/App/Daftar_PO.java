    private void Tampilkan_Data() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"ID", "Atas Nama", "Jenis Ikan", "Jumlah", "No Telepon", "Tgl Pemesanan", "Keterangan", "Status"});
        tbl_po.setModel(model);

        try {
            java.sql.Connection conn = Config.configDB();
            String sql = "SELECT * FROM preorder";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("atas_nama");
                String jenis = rs.getString("jenis_ikan");
                int jumlah = rs.getInt("jumlah");
                String nomer = rs.getString("no_telp");
                String tanggal = rs.getString("tanggal_pemesanan");
                String keterangan = rs.getString("keterangan");
                String status = rs.getString("status");

                model.addRow(new Object[]{id, nama, jenis, jumlah, nomer, tanggal, keterangan, status,});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void configureTableResizable() {
        // Nonaktifkan resize otomatis
        tbl_po.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        // Aktifkan resize manual kolom
        tbl_po.getTableHeader().setResizingAllowed(true);

        // Atur lebar awal kolom (sesuaikan angka sesuai kebutuhan)
        TableColumnModel columnModel = tbl_po.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(44);   // ID
        columnModel.getColumn(1).setPreferredWidth(70);  // nama
        columnModel.getColumn(2).setPreferredWidth(100);  // jenis
        columnModel.getColumn(3).setPreferredWidth(50);   // jumlah
        columnModel.getColumn(4).setPreferredWidth(100);  // no_telp
        columnModel.getColumn(5).setPreferredWidth(150);  // tanggal
        columnModel.getColumn(6).setPreferredWidth(150);  // keterangan
        columnModel.getColumn(7).setPreferredWidth(60);  // keterangan

        // Pastikan JScrollPane tidak menimpa pengaturan tabel
        JScrollPane scrollPane = (JScrollPane) tbl_po.getParent().getParent();
        scrollPane.setViewportView(tbl_po);
    }

