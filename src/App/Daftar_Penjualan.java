    private void Tampilkan_Data() {
        DefaultTableModel model = new DefaultTableModel();
        // Set column headers
        model.setColumnIdentifiers(new Object[]{"ID Penjualan", "Tanggal", "Total", "Cash", "Kembalian"});
        tbl_penjualan.setModel(model);

        // Setup currency formatting for Indonesian Rupiah
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        kursIndonesia.setDecimalFormatSymbols(formatRp);

        try {
            java.sql.Connection conn = Config.configDB();
            // Modified SQL query to get only sales data without id_akun
            String sql = "SELECT id, tanggal, total, cash, kembali FROM penjualan ORDER BY tanggal DESC";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                // Format currency values
                String totalFormatted = kursIndonesia.format(rs.getDouble("total"));
                String cashFormatted = kursIndonesia.format(rs.getDouble("cash"));
                String kembaliFormatted = kursIndonesia.format(rs.getDouble("kembali"));

                // Get values from result set
                int id = rs.getInt("id");
                String tanggal = rs.getString("tanggal");

                // Add row to table model with formatted currency values
                model.addRow(new Object[]{
                    id,
                    tanggal,
                    totalFormatted,
                    cashFormatted,
                    kembaliFormatted
                });
            }

            // Auto-resize columns to fit content
            for (int i = 0; i < tbl_penjualan.getColumnCount(); i++) {
                tbl_penjualan.getColumnModel().getColumn(i).setPreferredWidth(150);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

