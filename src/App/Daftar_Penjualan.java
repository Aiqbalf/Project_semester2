    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        // TODO add your handling code here:
        // Get the selected row from the table
        int selectedRow = tbl_penjualan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih penjualan yang akan dicetak terlebih dahulu!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get the ID Penjualan from selected row
        int idPenjualan = (int) tbl_penjualan.getValueAt(selectedRow, 0);

        try {
            // Buat koneksi ke database
            java.sql.Connection con = Config.configDB();

            // Load file report
            String reportPath = "src/Nota/notaDidiexs.jasper"; // Sesuaikan path
            JasperReport jasperReport = (JasperReport) JRLoader.loadObjectFromFile(reportPath);

            // Buat parameter ke report
            Map<String, Object> param = new HashMap<>();
            param.put("idpenjualan", idPenjualan);

            // Isi laporan dengan data
            JasperPrint print = JasperFillManager.fillReport(jasperReport, param, con);

            // Tampilkan laporan
            JasperViewer.viewReport(print, false);

        } catch (JRException ex) {
            JOptionPane.showMessageDialog(this,
                    "Gagal memuat file report: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Gagal mencetak nota: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    

    }//GEN-LAST:event_btn_cetakActionPerformed

